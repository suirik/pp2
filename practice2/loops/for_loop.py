# for loop examples

for i in range(5):
    print(i)

for i in range(1, 6):
    print(i)

for i in range(2, 11, 2):
    print(i)

# Loop through list
numbers = [1, 3, 5, 7]
for n in numbers:
    print(n)

# Loop with input
n = int(input("N: "))
for i in range(n):
    print(i)
 
